black = "\033[1;0;40m"

def red(message):
    print("\033[1;31;40m"+ message + black)

def grey(message):
    print("\033[1;30;40m" + message + black)

def green(message):
    print("\033[1;32;40m" + message + black)

def yellow(message):
    print("\033[1;33;40m" + message + black)

def blue(message):
    print("\033[1;34;40m" + message + black)

def cyan(message):
    print("\033[1;36;40m" + message + black)